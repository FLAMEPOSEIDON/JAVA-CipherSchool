
public class Loops {
    public static void main(String[] args) {
        int i;

        //for loop
        for(i=1;i<=100;i++)
        {
            System.out.println(i);
        }
        System.out.println(i);

        System.out.println("**********************");

        int n=5;
        // while(n>0){//infinite loop
        //     System.out.println("hi");
        // }
        System.out.println("**********************");


        //do-while loop
        do{
            System.out.println(n);
        }while(n>0);
    }
}
